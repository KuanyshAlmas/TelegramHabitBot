def get_text(key: str, lang: str = "kk", **kwargs) -> str:
    """Get translated text by key."""
    text_dict = TEXTS.get(key, {})
    text = text_dict.get(lang, text_dict.get("kk", key))

    if kwargs:
        try:
            text = text.format(**kwargs)
        except KeyError:
            pass

    return text


